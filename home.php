<p class="lead"><b>Namecoin</b> is a decentralized open source information registration and transfer system based on the Bitcoin cryptocurrency.
</p>
<div class="row">
    <div class="col-md-6">
      <h2>What does it do?</h2>
        <ul>
            <li>Securely record and transfer arbitrary names (keys).</li>
            <li>Attach a value (data) to the names<br> (up to 520 bytes, more in the future).</li>
            <li>Transact namecoins, the digital currency (&#x2115;, NMC).</li>
            <!-- Link to wiki page: properties of the namecoin blockchain-->
        </ul>
        <p><b>Namecoin</b> was the first fork of <a href="https://bitcoin.org">Bitcoin</a> and still is one of the most innovative altcoins.
        It was first to implement <a target="_blank" href="https://bitcoin.stackexchange.com/questions/273/how-does-merged-mining-work">merged mining</a> and a
        <a href="https://bit.namecoin.info">decentralized DNS</a>.
        <b>Namecoin</b> squares <a href="https://en.wikipedia.org/wiki/Zooko%27s_triangle">Zooko's Triangle</a>!</p>
    </div>
    <div class="col-md-6">
        <h2>What can it be used for?        </h2>
        <ul>
            <li>Protect free-speech rights online by making the web more resistant to censorship.</li>
            <li>Access websites using the <a href="https://bit.namecoin.info">.bit</a> domain (with TLS/SSL).</li>
            <li>Store <a href="https://nameid.org">identity information</a> such as email, GPG key, BTC address, TLS fingerprints, Bitmessage address, etc.</li>
            <li>Human readable Tor .onion names/domains.</li>
            <li>File signatures, Voting, bonds/stocks,/shares, web of trust, escrow and notary services (to be implemented).</li>
        </ul>
    </div>
</div>

